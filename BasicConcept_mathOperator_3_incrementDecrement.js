console.log('===========INCREMENT++++++++++++++++++++++')
//post increment
var a = 0
var b = 10
var a = b+=3
console.log(a)
console.log(b)

//pre increment
var a = 0
var b = 10
var a = a+=3+b
console.log(a)
console.log(b)

console.log('===========DECREMENT++++++++++++++++++++++')

//post increment
var a = 0
var b = 10
var a = b-=3
console.log(a)
console.log(b)

//pre increment
var a = 0
var b = 10
var a = a-=3-b
console.log(a)
console.log(b)
